#include <my_header.h>

/*
 * 功能：
 *     删除命令行参数指定的文件。
 *
 * unlink() 函数说明：
 *     原型：
 *         int unlink(const char *pathname);
 *
 *     参数：
 *         pathname
 *             要删除的文件路径，这里由 argv[1] 提供。
 *
 *     返回值：
 *         成功：返回 0
 *         失败：返回 -1，并设置 errno
 *
 * unlink() 到底做了什么：
 *     在 Linux/Unix 中，文件名和文件内容（inode）是分开的。
 *     unlink() 的本质不是“立刻擦除文件内容”，
 *     而是“删除一个文件名与 inode 之间的链接关系”。
 *
 *     你可以把它理解成：
 *         目录里保存的是“文件名 -> inode”的映射关系；
 *         unlink() 就是把这个文件名从目录项中移除。
 *
 * 为什么叫 unlink：
 *     un + link，意思就是“取消链接”。
 *
 * 重要理解：
 *     1. 如果一个文件只有这一个文件名（硬链接数为 1），
 *        那么 unlink 后，这个文件名消失。
 *
 *     2. 如果该文件还有别的硬链接，
 *        那么 unlink 只删除当前这个名字，
 *        文件数据并不会马上消失。
 *
 *     3. 如果某个进程此时还打开着这个文件，
 *        即使你 unlink 了，这个文件的数据也不会立刻真正释放；
 *        只有当：
 *            - 没有任何文件名链接到它
 *            - 并且没有进程再打开它
 *        这两个条件都满足时，内核才会真正回收文件占用的空间。
 *
 * 这也是 Linux 的一个重要机制：
 *     “文件删除后，已打开它的进程仍可继续使用它。”
 *
 * unlink() 可以删除什么：
 *     - 普通文件
 *     - 命名管道文件（FIFO）
 *     - 符号链接
 *
 * unlink() 不能直接删除目录：
 *     如果 pathname 是目录，unlink() 一般会失败。
 *     删除目录要用 rmdir()。
 *
 * 常见失败原因：
 *     1. argv[1] 没有提供，程序访问 argv[1] 会出错
 *     2. 文件不存在
 *     3. 权限不足
 *     4. 目标是目录
 *
 * 使用示例：
 *     假设之前创建了一个命名管道：
 *         ./a.out
 *     生成 2.pipe 后，可通过另一个程序：
 *         ./remove 2.pipe
 *     将该 FIFO 文件名从目录中删除。
 */

int main(int argc, char *argv[]){                                  
   
    // 删除 argv[1] 指定的文件
    // 成功返回 0，失败返回 -1
    int ret = unlink(argv[1]);
    printf("ret = %d\n", ret);
    ERROR_CHECK(ret, -1, "unlink failed");

    return 0;
}

