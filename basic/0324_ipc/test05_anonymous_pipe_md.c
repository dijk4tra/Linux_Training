#include <my_header.h>

/*
 * 什么是匿名管道？
 *   匿名管道由 pipe() 函数直接创建，本质上是内核中的一块缓冲区，
 *   同时内核会返回两个文件描述符：
 *      fd[0] -> 读端
 *      fd[1] -> 写端
 *
 * 为什么叫“匿名”？
 *   因为它没有文件系统中的名字，不会像普通文件那样出现在目录里。
 *   只能通过进程手里已有的文件描述符来访问它。
 *
 * 匿名管道最常见的使用方式：
 *   pipe() + fork()
 *
 * 原因：
 *   fork() 之后，子进程会复制父进程的文件描述符表，
 *   所以父子进程都会持有同一条管道的读端和写端，
 *   这就天然适合做父子进程之间的通信。
 *
 * 匿名管道的特点：
 *   1. 一般用于“有亲缘关系”的进程（如父子进程）通信
 *   2. 半双工（单向）通信：一条管道通常只负责一个方向
 *      - 要么 A 写，B 读
 *      - 要么 B 写，A 读
 *   3. 如果要双向通信，通常需要两条管道
 *
 * 匿名管道 和 命名管道（FIFO）的区别：
 *   1. 匿名管道：
 *      - 由 pipe() 创建
 *      - 没有路径名
 *      - 主要用于父子、兄弟等有亲缘关系的进程
 *      - 生命周期通常跟进程和文件描述符绑定
 *
 *   2. 命名管道（FIFO）：
 *      - 由 mkfifo() 创建
 *      - 在文件系统中有一个可见的路径名
 *      - 没有亲缘关系的两个进程，也可以通过这个路径打开它并通信
 *      - 只要 FIFO 文件还在，别的进程之后仍然可以打开它
 *
 * 为什么这里要用匿名管道，而不是命名管道？
 *   因为这个例子里通信双方就是 fork() 出来的父子进程，
 *   他们天然共享 pipe() 创建出来的文件描述符。
 *   用匿名管道最简单、最直接，不需要额外在文件系统创建 FIFO 文件。
 */

int main(int argc, char *argv[]){                                  
    
    int arr[2] = {0};
    /*
     * 创建一条匿名管道
     * arr[0] : 管道读端
     * arr[1] : 管道写端
     */
    pipe(arr);  // 创建了一条管道: 0下标是读文件描述符,1下标是写文件描述符

    pid_t ret = fork();

    if(ret == 0){
        
        /*
         * 子进程：
         *   负责向管道写数据
         *
         * 注意：
         *   子进程虽然同时也继承了 arr[0] 和 arr[1]，
         *   但本例中子进程只负责写，不负责读，
         *   所以应该关闭不用的读端 arr[0]。
         *
         * 为什么建议关闭不用的端？
         *   1. 节省文件描述符资源
         *   2. 明确当前进程的职责，避免误操作
         *   3. 对于很多管道程序，关闭无用端还能避免 read() / write()
         *      因“对端仍然被某个进程持有”而迟迟不结束
         */
        close(arr[0]);

        // arr[1]: 写文件描述符
        int write_fd = arr[1];

        char msg[] = "msg frommmmmmmm child.";
        /*
         * 向管道写数据
         * 父进程会从 arr[0] 中读到这些数据
         *
         * sizeof(msg) 包含字符串结尾的 '\0'，
         * 所以父进程读出来后可以直接按字符串打印。
         */
        write(write_fd, msg, sizeof(msg));

    }else{
        /*
         * 父进程：
         *   负责从管道读数据
         *
         * 父进程不写，所以关闭不用的写端 arr[1]
         */
        close(arr[1]);

        // arr[0]: 读文件描述符
        int read_fd = arr[0];
        char buf[100] = {0};
        /*
         * 从管道中读取子进程写入的数据
         * read() 会把数据放到 buf 中
         */
        read(read_fd, buf, sizeof(buf));

        printf("father process read from pipe: %s\n", buf);

    }
    
    return 0;
}

