#include <my_header.h>

/* Usage:  */
int main(int argc, char *argv[]){                                  
    
    /*
     * setsid()：创建一个新的会话（session），并让当前进程成为：
     * 1. 新会话的会话首进程（session leader）
     * 2. 新进程组的组长进程（process group leader）
     * 3. 脱离原来的控制终端
     *
     * 这是 Linux/Unix 中非常重要的一个系统调用，
     * 守护进程（daemon）的创建过程里经常会用到它。
     *
     * setsid() 调用成功后，效果可以这样理解：
     * - 当前子进程不再属于原来父进程所在的那个 session
     * - 当前子进程自己“开辟了一个全新的会话”
     * - 新会话的 SID 等于当前进程自己的 PID
     *
     * 也就是说，若子进程 PID 是 2000，
     * 那么 setsid() 成功后：
     * - 这个子进程的 SID 也通常是 2000
     *
     * ---------------------------------------
     * 为什么这里要在子进程里调用 setsid()？
     * ---------------------------------------
     * 因为 setsid() 有一个重要限制：
     * “调用进程不能是进程组组长”，否则调用失败。
     *
     * 通常 fork() 出来的子进程不是进程组组长，
     * 所以常见写法就是：
     * 1. 先 fork()
     * 2. 在子进程中调用 setsid()
     *
     * 这正是创建守护进程时的经典步骤之一。
     *
     * ---------------------------------------
     * setsid() 的返回值
     * ---------------------------------------
     * 成功：返回新会话的 SID
     * 失败：返回 -1
     *
     */

    if(fork() == 0){
        printf("child process, begin.\n");
        setsid();
        /*
         * while(1) 表示无限循环。
         *
         * 这里让子进程持续运行，
         * 目的是方便观察：即使父进程结束后，
         * 这个已经 setsid() 的子进程仍可继续存在。
         *
         * 由于它已经创建了自己的 session，
         * 它和原父进程的关系在“会话层面”上已经分离了。
         */
        while(1){
            printf("child process running.\n");
            sleep(1);
        }
    }else{
        /*
         * 父进程休眠 60 秒，然后结束。
         *
         * 观察点：
         * - 父进程会在 60 秒后退出
         * - 子进程由于进入死循环，会继续运行
         * - 而且子进程已经通过 setsid() 建立了新会话
         *
         */
        printf("father process, begin.\n");
        sleep(60);
        printf("father process over.\n");
    }
    
    return 0;
}

