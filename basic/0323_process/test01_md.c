    // fork()：
    // [执行流]：fork() 被调用一次，但会返回两次。
    //           父进程中返回子进程的 PID，子进程中返回 0。失败返回 -1。
    //
    // [内存层面]：早期的 Unix 会把父进程的所有内存完全复制一份给子进程，非常低效。
    //             现代 Linux 采用了 "写时拷贝 (Copy-on-Write, COW)" 技术：
    //             - fork 瞬间，内核为子进程创建进程控制块 (PCB)，并复制父进程的页表。
    //             - 此时，父子进程的虚拟内存映射到【同一块物理内存】，并且这块内存被标记为“只读”。
    //             - 只有当父进程或子进程试图“修改”某个变量时（触发缺页中断），操作系统才会真正在物理内存中复制出那一页，让修改者在副本上操作。
    //             - 这就意味着，如果只是读取数据，父子进程共享内存，fork 的速度极快。

int main(int argc, char *argv[]){                                  
    
    printf("fork before.\n");
    
    /*
     * fork() 的核心流程：
     *
     * 调用 fork() 时，操作系统会创建一个新的进程（子进程）。
     *
     * 可以把它理解成：
     * - 父进程运行到 fork() 这里
     * - 内核复制出一个“新的执行主体”——子进程
     * - 从 fork() 返回之后，父子进程都会继续执行 fork() 后面的代码
     *
     * 注意：
     * 不是“父进程执行完了再让子进程执行”，
     * 而是 fork() 返回后，父子进程都具备独立运行资格，
     * 由调度器决定谁先运行、谁后运行，所以输出顺序可能不固定。
     */

    pid_t ret = fork();
    
    /*
     * fork() 对内存做了什么？
     *
     * 从“逻辑效果”看：
     * - 子进程会得到父进程当前进程空间的一份副本
     * - 包括：代码段、数据段、BSS 段、堆、栈、全局变量、局部变量等
     *
     * 但从“实际实现”看，Linux 通常不会在 fork() 发生的瞬间就把整块内存真的复制一遍，
     * 而是采用一种高效机制：写时复制（Copy-On-Write, COW）
     *
     * 写时复制的意思是：
     * 1. fork() 刚返回时，父子进程先“共享同一份物理内存页”
     * 2. 这些页会被标记为只读
     * 3. 当父进程或子进程中某一方尝试修改某个内存页时，
     *    内核才会真正复制那一页，给修改者一份新的页
     * 4. 这样既保证了“父子进程彼此独立”，又避免了一开始的大量无意义复制
     *
     * 所以结论是：
     * - fork() 之后，父子进程看到的变量值一开始相同
     * - 但它们是两个独立的进程，之后各自修改变量，互不影响
     *
     * 例如：
     *   int x = 10;
     *   fork();
     *   // 父子进程里最开始看到的 x 都是 10
     *   // 如果子进程把 x 改成 20，不会影响父进程中的 x
     */
    
    /*
     * 通过 fork() 的返回值区分当前代码是在父进程还是子进程中运行
     *
     * fork() 返回值规则：
     * - 返回 0   ：当前在子进程中
     * - 返回 > 0 ：当前在父进程中，值为“子进程的 pid”
     * - 返回 -1  ：创建子进程失败
     */
    
    if (ret < 0) {
        /*
         * ret < 0 表示 fork() 失败
         * 常见原因可能是：
         * - 系统进程数达到上限
         * - 内存资源不足
         * - 用户可创建的进程数受限
         */
        perror("fork failed.\n");
        return -1;
    }

    if(ret == 0 ){
        // 说明在子进程中. 
        printf("child process, pid = %d, ppid = %d, ret = %d\n", getpid(), getppid(), ret);
        sleep(20);
        printf("child process, sleep over.\n");
    }else{
        // 说明在父进程中.
        printf("father process, pid = %d, ppid = %d, ret = %d\n", getpid(), getppid(), ret);
        sleep(5);
        printf("father process, sleep over.\n");
    }

    // 关于进程生命周期与 Shell 的关系
    // 启动这个程序的终端 shell（比如 bash）是当前父进程的父进程。
    // shell 进程默认只会 wait() 等待它直接派生出来的进程（即此处的父进程）结束。
    // 
    // [有趣的现象]：
    // 因为父进程只sleep 5秒，它会先于子进程结束。父进程结束后，shell 发现它要等的进程死掉了，就会重新弹出命令提示符，让你输入下一条命令。
    // 但此时子进程还在sleep(20)中。它失去了父父进程，变成了一个孤儿进程 (Orphan Process)。
    // Ubuntu 的系统 init 进程(通常是 systemd，PID为1)会自动“收养”这个孤儿进程。
    // 等20秒结束后，子进程打印的信息才会输出在终端屏幕上。


    return 0;
}

