#include <my_header.h>

/**
 * 知识点 1：不可重入函数 (Non-reentrant Function)
 * 特征：函数内部使用了静态变量 (static) 或全局变量。
 * 风险：在多线程环境下，多个线程同时访问同一个静态缓冲区 p，会产生竞态条件。
 * 即使是单线程，如果该函数在执行一半时被信号中断，而在信号处理函数中又调用了它，
 * 静态变量的值会被破坏，导致不可预知的结果。
 */
char *fun()
{
    // static 变量存储在数据段（Data Segment），生命周期贯穿整个程序
    static char p[20] = {0};
    for(int i = 0; i < 20; i++)
    {
        if(p[i] == 0)
        {
            p[i] = 'a'; // 修改了共享状态
            break;
        }
    } 
    // 返回指向静态区的指针。由于所有调用者拿到的都是同一个地址，
    // 后续调用会覆盖前序调用的结果。
    return p;
} 

/**
 * 知识点 2：可重入函数 (Reentrant Function)
 * 特征：不使用任何静态或全局变量。
 * 原理：所有的变量（如 a）都分配在栈（Stack）上。
 * 优势：每个调用该函数的线程/过程都会拥有自己独立的栈帧。
 * 即使函数被多次“重入”（同时执行），它们之间互不干扰，数据是安全的。
 */
int func()
{   
    // 局部变量，分配在当前线程/进程的栈空间
    int a = 10;
    ++a;
    return a; // 通过值传递返回，不涉及共享内存
}

int main(int argc,char* argv[])
{   
    // --- 不可重入函数演示 ---
    /* char *p1 = fun(); // p1 指向静态数组 p
       printf("p = %s \n", p1); 
       //第二次进入这个fun函数 
       char *p2 = fun(); 
       printf("p1 = %s, p2 = %s \n", p1, p2); // 会发现 p1 的内容也被改变了
    */

    // --- 可重入函数演示 ---
    // 每次调用 func，栈上都会创建一个新的变量 a，互不影响
    int x = func();
    printf("x = %d\n", x);
    int y = func();
    printf("y = %d\n", y);

    return 0;
}

/**
 * 1.什么是可重入性（Reentrancy）？
 *     一个函数被称为可重入的，是指它在执行过程中被中断
 *     (例如被信号处理函数中断，或在多线程中切换)，
 *     再次进入该函数执行时，不会导致数据错误或逻辑混乱。
 *
 * 2.判定标准（三要素）
 * 要使一个函数成为可重入函数，必须满足以下条件：
 *     a.不使用全局或静态变量：这些变量是进程内共享的，容易被并发访问破坏
 *     b.不返回指向静态数据的指针：如 fun() 返回的 p
 *     c.不调用不可重入的函数：例如 malloc、free、printf 在某些标准库实现
 *       中使用了全局锁或静态缓冲区，通常被视为不可重入
 *
 * 3.在 Linux 编程中的应用
 *     a.线程安全 (Thread-safe)：如果你的代码要在多线程环境下运行，
 *       必须保证函数是线程安全的。虽然“可重入”一定是“线程安全”的，
 *       但反之不一定（线程安全可以通过加锁实现，而可重入要求更高，
 *       连锁都不能依赖，因为锁可能导致死锁）。
 *     b.信号处理函数 (Signal Handler)：在 Linux 中，编写信号处理函数时，
 *       只能调用“异步信号安全”（Async-Signal-Safe）的函数，这些函数本质上都是可重入的。  
 */
