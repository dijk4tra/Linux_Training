#include <my_header.h>

int main(int argc, char *argv[]){                                  
    
    /*==============================================================
     * 一、服务器要绑定的 IP 和端口
     * 这里写死是为了演示。
     * 实际开发中常见做法：
     * 1. 从命令行参数读取
     * 2. 绑定 0.0.0.0（即 INADDR_ANY），表示监听本机所有网卡地址
     *==============================================================*/
    char *ip = "192.168.193.128";
    char *port = "12345";
    
    /*==============================================================
     * 二、创建套接字 socket
     * 
     * socket(AF_INET, SOCK_STREAM, 0);
     * 
     * 参数说明：
     * 1. AF_INET
     *    - 地址族，表示使用 IPv4
     * 2. SOCK_STREAM
     *    - 套接字类型，表示使用面向连接的字节流服务
     *    - 对应 TCP 协议
     * 3. 0
     *    - 协议号，通常写 0，表示让系统自动选择
     *    - 对于 AF_INET + SOCK_STREAM，系统会自动选择 TCP
     * 
     * 返回值：
     * - 成功：返回一个非负整数，称为“套接字文件描述符”
     * - 失败：返回 -1，并设置 errno
     *==============================================================*/
    int listen_fd = socket(AF_INET, SOCK_STREAM, 0);
    ERROR_CHECK(listen_fd, -1, "socket");
    
    /*==============================================================
     * 三、设置套接字选项：地址重用 / 端口重用
     * 
     * setsockopt 的作用：
     * - 给 socket 设置额外属性
     * 
     * SO_REUSEADDR 的常见用途：
     * - 服务器关闭后，端口可能暂时不能立即再次绑定(比如处于TIME_WAIT状态)
     * - 设置此选项后，可提高端口重新绑定的成功率
     * 
     * 参数说明：
     * 1. listen_fd: 要设置选项的 socket
     * 2. SOL_SOCKET: 表示该选项位于 socket 层
     * 3. SO_REUSEADDR: 地址重用选项
     * 4. &opt: 选项值的地址
     * 5. sizeof(opt): 选项值长度
     * 
     * 返回值：
     * - 成功：0
     * - 失败：-1
     *==============================================================*/
    int opt = 1;
    setsockopt(listen_fd, SOL_SOCKET, SO_REUSEADDR, &opt, sizeof(opt));
    
    /*==============================================================
     * 四、准备服务器地址结构体
     *
     * struct sockaddr_in 是 IPv4 专用地址结构体
     *
     * 其中最重要的三个成员：
     * 1. sin_family  -> 地址族
     * 2. sin_port    -> 端口号（网络字节序）
     * 3. sin_addr    -> IP 地址（网络字节序）
     *
     * client_addr 用于 accept 时接收客户端地址信息
     * len 作为 accept 的“输入输出参数”
     *==============================================================*/
    struct sockaddr_in addr, client_addr;
    socklen_t len = sizeof(client_addr);
    
    /*==============================================================
     * 五、先清零结构体
     * - 防止结构体中未赋值成员是随机垃圾值
     * - 某些填充字节 / 保留字段清零更安全
     *==============================================================*/
    memset(&addr, 0, sizeof(addr));
    
    /*==============================================================
     * 六、填写服务器地址
     *==============================================================*/
    // 1. 指定地址族：IPv4
    addr.sin_family = AF_INET;
    // 2. 指定 IP 地址
    // inet_addr 的作用：
    // - 把点分十进制字符串形式的 IPv4 地址
    // - 转换成 32 位二进制形式
    // - 结果按网络字节序存入 sin_addr.s_addr
    addr.sin_addr.s_addr = inet_addr(ip); 
    // 3. 指定端口号
    // atoi(port)：
    // - 把字符串 "12345" 转成整数 12345
    //
    // htons(...)：
    // - host to network short
    // - 把主机字节序的 16 位整数转成网络字节序
    addr.sin_port = htons(atoi(port));
    
    /*==============================================================
     * 七、bind：绑定本机 IP 和端口
     *
     * bind 的作用：
     * - 把该 socket 和“本机地址（IP + port）”绑定起来
     *
     * 函数原型：
     * int bind(int sockfd, const struct sockaddr *addr, socklen_t addrlen);
     *
     * 为什么这里要强制类型转换？
     * - bind 的第二个参数要求是 struct sockaddr *
     * - 但我们实际使用的是 IPv4 专用结构体 struct sockaddr_in *
     * - 两者不是同一种指针类型，所以需要显式转换
     *
     * (struct sockaddr *)&server_addr
     * 的本质：
     * - 把 IPv4 地址结构体指针，转换成通用地址结构体指针
     *
     * 返回值：
     * - 成功：0
     * - 失败：-1
     *
     *==============================================================*/
    int ret = bind(listen_fd, (struct sockaddr *)&addr, sizeof(addr));
    ERROR_CHECK(ret, -1, "bind");
    
    /*==============================================================
     * 八、listen：把 socket 变成监听套接字
     *
     * listen 的作用：
     * - 告诉内核：这是一个服务器监听 socket
     * - 可以开始接收客户端的连接请求
     *
     * 第二个参数 backlog：
     * - 表示监听队列长度的参考值
     * - 可简单理解为“排队等待处理的连接容量”
     *
     * 返回值：
     * - 成功：0
     * - 失败：-1
     *
     * 注意：
     * - 调用 listen 后，服务器进入 LISTEN 状态
     * - 到这里仍然没有发生三次握手
     *==============================================================*/
    ret = listen(listen_fd, 10);
    ERROR_CHECK(ret, -1, "listen");
    printf("server is listening...\n");
    
    /*==============================================================
     * 九、accept：接受客户端连接
     *
     * accept 的作用：
     * - 从“已完成连接队列”中取出一条已经建立好的 TCP 连接
     * - 返回一个新的文件描述符 conn_fd，专门用于和该客户端通信
     *
     * 重要区分：
     * 1. listen_fd
     *    - 只负责监听，等待新的客户端连接
     *
     * 2. conn_fd
     *    - 只负责和某一个客户端通信
     *
     * accept 参数说明：
     * 1. listen_fd
     *    - 监听套接字
     *
     * 2. (struct sockaddr *)&client_addr
     *    - 用来接收客户端地址信息
     *
     * 3. &len
     *    - 输入输出型参数
     *    - 调用前：表示 client_addr 缓冲区大小
     *    - 调用后：表示实际写入的地址长度
     *
     * 返回值：
     * - 成功：返回新的通信 fd（conn_fd）
     * - 失败：返回 -1
     *
     * 与三次握手的关系：
     * - 三次握手主要由内核协议栈完成
     * - 当握手完成后，连接会进入已完成连接队列
     * - accept 的作用是“把这条已经建立好的连接取出来”
     *==============================================================*/
    int conn_fd = accept(listen_fd, (struct sockaddr *)&client_addr, &len);
    ERROR_CHECK(conn_fd, -1, "accept");
    
    /*==============================================================
     * 十、打印客户端信息（便于学习观察）
     *
     * inet_ntoa(client_addr.sin_addr)
     * - 把二进制 IPv4 地址转成点分十进制字符串
     *
     * ntohs(client_addr.sin_port)
     * - network to host short
     * - 把网络字节序端口转回主机字节序
     *==============================================================*/
    printf("client ip = %s, client port = %d\n",
           inet_ntoa(client_addr.sin_addr),
           ntohs(client_addr.sin_port));

    /*==============================================================
     * 十一、接收客户端发送的数据
     *
     * recv 函数原型：
     * ssize_t recv(int sockfd, void *buf, size_t len, int flags);
     *
     * 参数说明：
     * 1. conn_fd
     *    - 通信套接字
     *
     * 2. buf
     *    - 接收缓冲区
     *
     * 3. sizeof(buf) - 1
     *    - 最多接收多少字节
     *    - 这里减 1 是为了给字符串结束符 '\0' 留空间
     *
     * 4. 0
     *    - flags，通常写 0，表示默认行为
     *
     * 返回值：
     * - > 0：实际接收到的字节数
     * - = 0：对端正常关闭连接
     * - = -1：出错
     *
     * 注意：
     * - TCP 是字节流，没有“消息边界”
     * - 一次 send，不一定正好对应一次 recv
     *==============================================================*/
    char buf[100] = {0};
    // 阻塞等待客户端发送数据，返回值 cnt 是实际接收到的字节数
    int cnt = recv(conn_fd, buf, sizeof(buf)-1, 0);
    if (cnt == 0) {
        printf("client has closed the connection\n");
    } else {
        buf[cnt] = '\0';   // 把收到的数据补成字符串，便于打印
        printf("recv cnt = %d\n", cnt);
        printf("server recv: %s\n", buf);
    }
    
    /*==============================================================
     * 十二、服务器向客户端发送数据
     *
     * send 函数原型：
     * ssize_t send(int sockfd, const void *buf, size_t len, int flags);
     *
     * 参数说明：
     * 1. conn_fd
     *    - 通信套接字
     *
     * 2. "byebye"
     *    - 待发送数据首地址
     *
     * 3. 6
     *    - 发送 6 个字节，不包含 '\0'
     *
     * 4. 0
     *    - 默认发送标志
     *
     * 返回值：
     * - >= 0：实际发送字节数
     * - -1 ：发送失败
     *
     * 注意：
     * - send 也不保证一次就把所有数据都发完
     * - 这里只是基础教学示例，因此暂不写循环发送
     *==============================================================*/
    cnt = send(conn_fd, "byebye", 6, 0); // 发送 6 个字节的数据i
    ERROR_CHECK(cnt, -1, "send");
    printf("send cnt: %d\n", cnt);
    
    /*==============================================================
     * 十三、关闭连接
     *
     * close(conn_fd)：
     * - 关闭与当前客户端的通信连接
     * - 若条件满足，会由内核发起 TCP 连接关闭流程
     *
     * close(listen_fd)：
     * - 关闭监听套接字
     * - 之后服务器不能再 accept 新连接
     *
     * 与四次挥手的关系：
     * - close 不是“四次挥手本身”
     * - 更准确地说，close 是应用层发起关闭请求
     * - 后续 FIN / ACK 报文交换由内核协议栈完成
     *==============================================================*/
    close(listen_fd); // 停止监听新的连接
    close(conn_fd);   // 关闭当前通信连接（向客户端发送 FIN 报文，触发四次挥手）
    return 0;
}

