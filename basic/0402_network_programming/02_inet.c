#include <my_header.h>

/*
 * 1. IP 地址的两种形态
 *    表现形式(点分十进制): 方便人类记忆和阅读，
 *    例如"192.168.100.100"。它是一个字符串，占用十几个字节。
 *    
 *    存储形式(32位整数): IPv4 地址本质上是由 4 个字节（32个比特位）组成的。
 *    例如 192 对应二进制 11000000，168 对应 10101000。
 *    计算机将这 4 个字节拼接成一个 32 位整数进行网络传输，极其高效。
 *
 * 2. 转换函数族
 *    inet_aton (a to n): a 代表 ASCII（字符串），n 代表 Network（网络字节序）。
 *    它不仅完成了字符串到数字的转换，还自动确保了生成的数字是大端序的，符合网络传输标准。
 *
 *    inet_ntoa (n to a): 作用相反。需要注意的是，这个函数内部使用了一块静态内存来保存返回的字符串。
 *    如果在同一个打印语句中调用两次 inet_ntoa，后一次的结果会覆盖前一次的结果。
 *
 */



int main(int argc, char *argv[]){                                  
    
    // 点分十进制的 IP 地址字符串
    char *ptmp = "192.168.100.100"; 
    
    // struct in_addr 是网络编程中专门用来存储 IP 地址的结构体
    // 内部其实就是一个 32 位的无符号整数 (in_addr_t s_addr)
    struct in_addr addr;
    
    // inet_aton (ASCII to Network)
    // 将点分十进制的字符串转换为网络字节序（大端序）的 32 位整数，并存入 addr 中
    // 如果转换成功返回非 0，失败返回 0
    inet_aton(ptmp, &addr); 

    // 注意这里打印的结果：
    // addr.s_addr 已经是网络字节序（大端序 0xC0A86464）。
    // 如果你的电脑是小端序（大多数个人电脑都是），直接用 %d 打印时，
    // CPU 会以小端序的方式去逆向解读这块内存，打印出来会是一个看起来很奇怪的十进制大数字。
    printf("十进制的地址: %d\n", addr.s_addr); // IP 地址是无符号的，建议用 %u
    
    // inet_ntoa (Network to ASCII)
    // 将网络字节序的 32 位整数，反向转换回点分十进制的字符串
    char *pc = inet_ntoa(addr);
    printf("pc = %s\n", pc);

    return 0;
}
